-- Add some realistic booking patterns (some seats booked/locked)
-- Book 10-15% of seats randomly
UPDATE seat SET status = 'BOOKED' 
WHERE id IN (
    SELECT id FROM (
        SELECT id FROM seat 
        WHERE RAND() < 0.12 
        ORDER BY RAND() 
        LIMIT 1000
    ) AS random_seats
);

-- Lock 2-3% of seats (temporary holds)
UPDATE seat SET status = 'LOCKED' 
WHERE status = 'AVAILABLE' AND id IN (
    SELECT id FROM (
        SELECT id FROM seat 
        WHERE status = 'AVAILABLE' AND RAND() < 0.025
        ORDER BY RAND() 
        LIMIT 200
    ) AS random_locked_seats
);

