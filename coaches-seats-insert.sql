-- Coach type breakdown
SELECT 'Coach types breakdown:' AS info;
SELECT coach_type, COUNT(*) as coach_count, 
       (SELECT COUNT(*) FROM seat WHERE seat.coach_id = coach.id) as total_seats
FROM coach 
GROUP BY coach_type 
ORDER BY coach_type;

-- Sample data verification
SELECT 'Sample coach and seat data:' AS info;
SELECT c.id as coach_id, c.coach_number, c.coach_type, c.journey_id,
       COUNT(s.id) as seat_count,
       SUM(CASE WHEN s.status = 'AVAILABLE' THEN 1 ELSE 0 END) as available_seats,
       SUM(CASE WHEN s.status = 'BOOKED' THEN 1 ELSE 0 END) as booked_seats
FROM coach c
LEFT JOIN seat s ON c.id = s.coach_id
GROUP BY c.id, c.coach_number, c.coach_type, c.journey_id
ORDER BY c.journey_id, c.coach_number
LIMIT 20;

